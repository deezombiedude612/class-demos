usr_input = int(input("Enter a number from 0 to 4 >> "))

if usr_input == 0:
    print("ZERO")

elif usr_input == 1:
    print("ONE")

elif usr_input == 2:
    print("TWO")

elif usr_input == 3:
    print("THREE")

elif usr_input == 4:
    print("FOUR")

else:
    print("Out of range! Valid number is from 0 to 4 only")
