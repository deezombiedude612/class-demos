input_number = int(input("Enter a number from 0 to 4 >> "))

if input_number == 0:
    print("ZERO")
elif input_number == 1:
    print("ONE")
elif input_number == 2:
    print("TWO")
elif input_number == 3:
    print("THREE")
elif input_number == 4:
    print("FOUR")
else:
    print("Out of range! Valid number is from 0 to 4 only")
